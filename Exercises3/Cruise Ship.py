cc = input("Enter your cabin class: ")

if cc == "Lux":
    print("You are on the upper deck cabin with a balcony!")

elif cc == "A":
    print("You are above the car deck, equipped with a window.")

elif cc == "B":
    print("You are on the windowless cabin above the car deck.")

elif cc == "C":
    print("You are on the windowless cabin below the car deck.")

else:
    print("Invalid cabin class")






